    //===================================================================================
    private void fillElements(int[][] a, String[] names, int n, DefaultListModel model) {
        for (int i = 0; i < n; ++i) {
            String element = names[i];
            for (int j = 1; j <= a[i][0]; ++j) {
                element += "   " + a[i][j];
            }
            model.addElement(element);
        }
    }
    
    
