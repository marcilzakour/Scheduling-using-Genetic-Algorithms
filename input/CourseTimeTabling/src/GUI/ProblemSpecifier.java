    private int addNewSlots(int[][] times, int numSlots, int index) {

        int suit[] = new int[numSlots + 1];
        StringTokenizer st;
        int maxSlots = (int) jSpinner1.getValue();
        boolean done = true;
        while (done) {
            try {
                String str = JOptionPane.showInputDialog(null, "Enter all " + numSlots + " suitable time slots:");
                if( str == null ) return -1 ;
                st = new StringTokenizer(str);

                if (st.countTokens() < numSlots || st.countTokens() > numSlots) {
                    JOptionPane.showMessageDialog(null, " Number of slots must be " + numSlots);
                    continue;
                }

                for (int i = 1; st.hasMoreTokens(); i++) {
                    suit[i] = Integer.parseInt(st.nextToken());
                    if (suit[i] > maxSlots) {
                        throw new FileNotFoundException();
                    }
                    times[ index][i] = suit[i];
                }
                times[ index][0] = numSlots;
                done = false;
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(null, "please enter just numbers.");
            } catch (FileNotFoundException ex) {
                JOptionPane.showMessageDialog(null, "slots should be less than max slots available : "
                        + maxSlots);
            }
        }
return 1;
    }

